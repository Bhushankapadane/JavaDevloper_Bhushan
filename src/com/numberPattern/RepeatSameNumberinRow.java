package com.numberPattern;

public class RepeatSameNumberinRow {
//	1
//	22
//	333
//	4444
//	55555
//	666666
	
	public static void main(String[] args) {
		for(int i=0;i<7;i++) {
			
			for(int j=0;j<i;j++) {
				
				System.out.print(i);
			}
			
			
			System.out.println();
			
			
			
		}
		
		
	}
}
